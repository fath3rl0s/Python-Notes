#Complex Numbers
numbComplex = 3.14j
print(numbComplex)
print(type(numbComplex))

print("\n")



#Hex Base
t1_hex = 0xa
print(t1_hex)
print(type(t1_hex))
print("\n")


#Octal Representation
t1_octal = 0o10
print(t1_octal)
print(type(t1_octal))
print("\n")



# Add int, hex, octal
print(1 + 0x1 + 0o1)
print("\n")


# Print the Absolute value (built-in function to measure distance between number and zero); abs
print(abs(4))
print(abs(-4))
print("\n")


# Rounding function; round
print(round(8.4))
print(round(8.5))
print(round(8.6))



# Binary Conversions; bin
print(bin(8))
print(bin(255))



# Hex Conversions; hex
print(hex(8))
print(hex(45))















